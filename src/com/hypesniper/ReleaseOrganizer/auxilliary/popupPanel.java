    public void setLabels(Release rel) {
        String date= " - ";
        SimpleDateFormat dayOfWeek =new SimpleDateFormat("EEEE, HH:mm");
        if (rel.getDate() != null) { //Detect if date is within a week
            Date now = new Date();
            Date relDate = rel.getDate();
            long diff = relDate.getTime() - now.getTime();
            long days = TimeUnit.DAYS.convert(diff, TimeUnit.MILLISECONDS);
            // if between next week, display day of week instead of year-month format
            if(days < 7 && days > 1) date = "Next "+dayOfWeek.format(relDate).replace(", 00:00", "");
            else date = rel.getFormattedDate();
        }

        dispBotable.setText(rel.getBot().equals("") ? " - " : rel.getBot());
        dispDate.setText(date);//rel.getFormattedDate().equals("") ? " - " : rel.getFormattedDate());
        dispShopify.setText(rel.getShopify().equals("") ? " - " : rel.getShopify());
        dispQueue.setText(rel.getQueue().equals("") ? " - " : rel.getQueue());
        dispNotes.setText(rel.getNotes().equals("") ? " - " : rel.getNotes());

        if (rel.getisEarlyLink()) {
            lblLink.setText("Early Link :");
        }

        dispLink.setText(rel.getLink());
    }

