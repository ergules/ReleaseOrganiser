    private void dispLinkMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dispLinkMouseClicked
        // TODO add your handling code here:
        if (SwingUtilities.isLeftMouseButton(evt)) { //left click opens in browser
            if (Desktop.isDesktopSupported()) {
                try {
                    Desktop.getDesktop().browse(new URI( dispLink.getText()));
                } catch (Exception ex) {
                    ex.printStackTrace();
                }
            }
        } else if (SwingUtilities.isRightMouseButton(evt)) { //right click copies to clipboard
            Clipboard clpbrd = Toolkit.getDefaultToolkit().getSystemClipboard();
            StringSelection stringSelection = new StringSelection(dispLink.getText());
            clpbrd.setContents(stringSelection, null);
            JOptionPane.showMessageDialog(dispQueue, "Link copied to Clipboard", "Copy to Clipboard", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_dispLinkMouseClicked

